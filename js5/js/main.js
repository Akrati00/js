// Math Methods and Properties

console.log(Math.PI);

console.log(Math.trunc(Math.PI)); //3 // returns the integer portion of the number and truncates any decimals

console.log(Math.round(Math.PI)); // 3 round the number to the nearest integer
console.log(Math.round(3.5)); // 4

console.log(Math.ceil(Math.PI)); // 4 rounded up the number
console.log(Math.floor(Math.PI)); // 4 rounded down the number

console.log(Math.pow(2,4)); // 16 

console.log(Math.min(1,0.5,9)); // 0.5
console.log(Math.max(3,5,6)); // 6

// random - generate random number between 0 to 1 which includes 0 but not 1
console.log(Math.random());

// 1 to 10
console.log(Math.floor(Math.random()*10) + 1); 

